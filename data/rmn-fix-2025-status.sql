-- RMN 2025년 12월 집행분 상태 보정 ('26.7)
-- 배경: 2025 시드(rmn-seed-2025.sql) 이관 시 대장 상태가 "예정"이던 12월 캠페인 9행을
--   status='집행'으로 넣었는데, 지금은 집행 종료 7개월 경과라 "진행 중" 목록에 남아 있음.
--   → 2025년 말 집행 종료분이므로 모두 '완료'로 정정.
-- 실행: Supabase SQL Editor 1회 (이미 rmn-seed-2025.sql을 실행한 경우에만 필요).
--   특정 건에 미수금이 실제로 남아 있으면, 반영 후 RMN 목록에서 그 건만 상태를 내릴 것.
update rmn_bookings set status = '완료'
where status = '집행'
  and start_date >= '2025-12-01' and start_date < '2026-01-01';
-- 결과: 9행 갱신 (예거르쿨트르 2 · 랜드로버 디펜더,벨라 2 · 부쉐론 4 · 삼성닷컴 1)
